# Operaciones Matematicas

x = float(input("Dame el valor de x: "))
y = float(input("Dame el valor de y: "))
        
        
suma = x + y
resta = x - y
multiplicacion = x * y
div = x / y
resid = x % y
exp = x ** y
dive = x // y

print (f"Suma             :{suma}")
print (f"Resta            :{resta}")
print (f"Multiplicacion   :{multiplicacion}")
print (f"Division         :{div}")
print (f"Residuo          :{resid}")
print (f"Exponienciacion  :{exp}")
print (f"Division Entera  :{dive}")





print(f"Suma {suma}\nResta {resta}\nMultiplicacion {multiplicacion}\nDivision {div}\n")
print(f"Residuo {resid}\nExponienciacion {exp}\nDivision Entera {dive}")

